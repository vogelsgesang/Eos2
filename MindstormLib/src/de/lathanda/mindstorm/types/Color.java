package de.lathanda.mindstorm.types;

public interface Color {
	public static final Type NONE   = new LC0(0x01); 
	public static final Type BLACK  = new LC0(0x01); 
	public static final Type BLUE   = new LC0(0x02);
	public static final Type GREEN  = new LC0(0x03); 
	public static final Type YELLOW = new LC0(0x04); 
	public static final Type RED    = new LC0(0x05); 
	public static final Type WHITE  = new LC0(0x06); 
	public static final Type BROWN  = new LC0(0x07);
}
